// ─── Memory Engine Types ───

export type MemoryType =
  | 'long-term'
  | 'short-term'
  | 'episodic'
  | 'semantic'
  | 'context'
  | 'project'
  | 'user'
  | 'team'
  | 'conversation';

export type MemorySource =
  | 'chat'
  | 'file'
  | 'meeting'
  | 'email'
  | 'website'
  | 'pdf'
  | 'document'
  | 'manual';

export type RelationType =
  | 'relates_to'
  | 'depends_on'
  | 'causes'
  | 'part_of'
  | 'contradicts'
  | 'supports'
  | 'derived_from'
  | 'references';

export type ExtractedType =
  | 'fact'
  | 'task'
  | 'preference'
  | 'goal'
  | 'contact'
  | 'company'
  | 'requirement'
  | 'decision';

export type ExtractionStatus = 'pending' | 'confirmed' | 'rejected';

export type TimelineEventType =
  | 'created'
  | 'updated'
  | 'referenced'
  | 'shared'
  | 'extracted'
  | 'deleted';

export type SearchType = 'all' | 'vector' | 'graph' | 'semantic' | 'keyword';

export interface MemoryNode {
  id: string;
  type: MemoryType;
  title: string;
  content: string;
  summary: string;
  source: MemorySource;
  sourceId: string;
  tags: string[];
  concepts: string[];
  confidence: number;
  importance: number;
  accessCount: number;
  agentId: string;
  agentAccess: string[];
  projectId: string;
  teamId: string;
  userId: string;
  vectorEmbedding: number[];
  createdAt: number;
  updatedAt: number;
  lastReferencedAt: number;
  metadata: Record<string, unknown>;
}

export interface MemoryRelation {
  id: string;
  sourceId: string;
  targetId: string;
  relationType: RelationType;
  strength: number;
  autoGenerated: boolean;
  createdAt: number;
}

export interface MemoryExtraction {
  id: string;
  sourceType: string;
  sourceContent: string;
  sourceId: string;
  extractedType: ExtractedType;
  extractedContent: string;
  confidence: number;
  status: ExtractionStatus;
  agentId: string;
  createdAt: number;
}

export interface MemoryTimelineEvent {
  id: string;
  memoryId: string;
  eventType: TimelineEventType;
  agentId: string;
  description: string;
  metadata: Record<string, unknown>;
  createdAt: number;
}

export interface MemorySearchResult {
  memoryId: string;
  score: number;
  searchType: SearchType;
  snippet: string;
}

export type GraphNodeType =
  | 'memory'
  | 'person'
  | 'company'
  | 'project'
  | 'task'
  | 'document'
  | 'concept'
  | 'decision';

export interface GraphNode {
  id: string;
  label: string;
  type: GraphNodeType;
  memoryId?: string;
  properties: Record<string, string>;
  color: string;
  size: number;
  x?: number;
  y?: number;
  vx?: number;
  vy?: number;
}

export interface GraphEdge {
  id: string;
  source: string;
  target: string;
  label: string;
  weight: number;
  color: string;
}

// ─── Store Interface ───

interface MemoryState {
  // State
  memories: MemoryNode[];
  memoryRelations: MemoryRelation[];
  memoryExtractions: MemoryExtraction[];
  memoryTimeline: MemoryTimelineEvent[];
  graphNodes: GraphNode[];
  graphEdges: GraphEdge[];
  memorySearchResults: MemorySearchResult[];
  memorySearchQuery: string;
  memorySearchType: SearchType;
  selectedMemoryId: string | null;
  memoryGraphLayout: 'force' | 'radial' | 'hierarchical';

  // Actions — Memory CRUD
  addMemory: (memory: MemoryNode) => void;
  updateMemory: (id: string, updates: Partial<MemoryNode>) => void;
  deleteMemory: (id: string) => void;
  referenceMemory: (id: string) => void;

  // Actions — Relations
  addMemoryRelation: (relation: MemoryRelation) => void;
  removeMemoryRelation: (id: string) => void;

  // Actions — Extractions
  addExtraction: (extraction: MemoryExtraction) => void;
  confirmExtraction: (id: string) => void;
  rejectExtraction: (id: string) => void;

  // Actions — Timeline
  addTimelineEvent: (event: MemoryTimelineEvent) => void;

  // Actions — Search
  searchMemories: (query: string) => void;

  // Actions — Getters
  getMemoryById: (id: string) => MemoryNode | undefined;
  getMemoriesByType: (type: MemoryType) => MemoryNode[];
  getMemoriesByAgent: (agentId: string) => MemoryNode[];
  getRelatedMemories: (memoryId: string) => MemoryRelation[];

  // Actions — Graph
  rebuildGraph: () => void;

  // Actions — Setters
  setMemorySearchQuery: (query: string) => void;
  setMemorySearchType: (type: SearchType) => void;
  setSelectedMemoryId: (id: string | null) => void;
  setMemoryGraphLayout: (layout: 'force' | 'radial' | 'hierarchical') => void;
}

