// ─── Search Engine ───

function performSearch(
  memories: MemoryNode[],
  query: string,
  searchType: SearchType,
): MemorySearchResult[] {
  if (!query.trim()) return [];

  const lowerQuery = query.toLowerCase().trim();
  const queryWords = lowerQuery.split(/\s+/).filter((w) => w.length > 1);
  const results: MemorySearchResult[] = [];

  for (const mem of memories) {
    let score = 0;
    const lowerTitle = mem.title.toLowerCase();
    const lowerContent = mem.content.toLowerCase();
    const lowerTags = mem.tags.map((t) => t.toLowerCase());
    const lowerConcepts = mem.concepts.map((c) => c.toLowerCase());
    const lowerSummary = mem.summary.toLowerCase();

    // ── Exact title match (highest score) ──
    if (lowerTitle === lowerQuery) {
      score += 100;
    } else if (lowerTitle.includes(lowerQuery)) {
      score += 70;
    } else {
      // Partial title word matches
      for (const word of queryWords) {
        if (lowerTitle.includes(word)) {
          score += 20;
        }
      }
    }

    // ── Exact tag match ──
    for (const tag of lowerTags) {
      if (tag === lowerQuery) {
        score += 60;
      } else if (queryWords.some((w) => tag.includes(w))) {
        score += 25;
      }
    }

    // ── Exact concept match ──
    for (const concept of lowerConcepts) {
      if (concept === lowerQuery) {
        score += 50;
      } else if (queryWords.some((w) => concept.includes(w))) {
        score += 20;
      }
    }

    // ── Content / summary partial match ──
    if (lowerContent.includes(lowerQuery)) {
      score += 40;
    } else if (lowerSummary.includes(lowerQuery)) {
      score += 30;
    } else {
      // Word-level content matching
      let contentWordMatches = 0;
      for (const word of queryWords) {
        if (lowerContent.includes(word)) {
          contentWordMatches++;
        }
      }
      score += contentWordMatches * 8;

      let summaryWordMatches = 0;
      for (const word of queryWords) {
        if (lowerSummary.includes(word)) {
          summaryWordMatches++;
        }
      }
      score += summaryWordMatches * 6;
    }

    // ── Boost by importance and confidence ──
    score += mem.importance * 10;
    score += mem.confidence * 5;

    // ── Boost by recency ──
    const ageHours = (now - mem.lastReferencedAt) / HOUR;
    if (ageHours < 1) score += 15;
    else if (ageHours < 6) score += 10;
    else if (ageHours < 24) score += 5;

    // ── Boost by access count (popular memories) ──
    if (mem.accessCount > 50) score += 10;
    else if (mem.accessCount > 20) score += 5;

    if (score > 0) {
      // Generate a snippet from content around the first match
      let snippet = '';
      const idx = lowerContent.indexOf(lowerQuery);
      if (idx >= 0) {
        const start = Math.max(0, idx - 40);
        const end = Math.min(mem.content.length, idx + lowerQuery.length + 60);
        snippet =
          (start > 0 ? '...' : '') +
          mem.content.slice(start, end) +
          (end < mem.content.length ? '...' : '');
      } else {
        // Use first 120 chars of summary as snippet
        snippet = mem.summary.slice(0, 120) + (mem.summary.length > 120 ? '...' : '');
      }

      // Determine effective search type label
      let effectiveSearchType: SearchType = searchType;
      if (searchType === 'all') {
        if (lowerTitle.includes(lowerQuery) || lowerTags.some((t) => t.includes(lowerQuery))) {
          effectiveSearchType = 'keyword';
        } else if (lowerConcepts.some((c) => c.includes(lowerQuery))) {
          effectiveSearchType = 'semantic';
        } else {
          effectiveSearchType = 'keyword';
        }
      }

      results.push({
        memoryId: mem.id,
        score: Math.round(score * 100) / 100,
        searchType: effectiveSearchType,
        snippet,
      });
    }
  }

  // Sort by score descending
  results.sort((a, b) => b.score - a.score);

  return results;
}

// ─── Graph Builder ───

function buildGraphFromMemories(
  memories: MemoryNode[],
  relations: MemoryRelation[],
): { nodes: GraphNode[]; edges: GraphEdge[] } {
  const nodes: GraphNode[] = [];
  const edges: GraphEdge[] = [];

  // Create memory nodes
  for (const mem of memories) {
    nodes.push({
      id: `node-${mem.id}`,
      label: mem.title.length > 30 ? mem.title.slice(0, 30) + '...' : mem.title,
      type: 'memory',
      memoryId: mem.id,
      properties: {
        memoryType: mem.type,
        source: mem.source,
        agentId: mem.agentId,
        importance: mem.importance.toFixed(2),
      },
      color: MEMORY_TYPE_COLORS[mem.type],
      size: 8 + mem.importance * 12 + Math.min(mem.accessCount / 10, 8),
      x: Math.random() * 600 - 300,
      y: Math.random() * 600 - 300,
    });
  }

  // Extract unique agents as person nodes
  const agentIds = [...new Set(memories.map((m) => m.agentId))];
  for (const agentId of agentIds) {
    nodes.push({
      id: `node-agent-${agentId}`,
      label: agentId.charAt(0).toUpperCase() + agentId.slice(1),
      type: 'person',
      properties: { role: 'agent' },
      color: GRAPH_COLORS.person,
      size: 16,
      x: Math.random() * 600 - 300,
      y: Math.random() * 600 - 300,
    });
  }

  // Extract unique concepts as concept nodes
  const allConcepts = [...new Set(memories.flatMap((m) => m.concepts))];
  // Only create concept nodes for concepts that appear in 2+ memories
  const conceptCounts: Record<string, number> = {};
  for (const c of allConcepts) {
    conceptCounts[c] = (conceptCounts[c] || 0) + 1;
  }
  const sharedConcepts = allConcepts.filter((c) => (conceptCounts[c] || 0) >= 2);
  for (const concept of sharedConcepts.slice(0, 15)) {
    // limit to 15 concept nodes
    nodes.push({
      id: `node-concept-${concept.replace(/\s+/g, '-')}`,
      label: concept,
      type: 'concept',
      properties: { count: String(conceptCounts[concept] || 0) },
      color: GRAPH_COLORS.concept,
      size: 6 + (conceptCounts[concept] || 0) * 2,
      x: Math.random() * 600 - 300,
      y: Math.random() * 600 - 300,
    });
  }

  // Extract projects
  const projectIds = [...new Set(memories.filter((m) => m.projectId).map((m) => m.projectId))];
  for (const projId of projectIds) {
    nodes.push({
      id: `node-project-${projId}`,
      label: projId.replace(/proj-/, '').replace(/-/g, ' '),
      type: 'project',
      properties: {},
      color: GRAPH_COLORS.project,
      size: 14,
      x: Math.random() * 600 - 300,
      y: Math.random() * 600 - 300,
    });
  }

  // Create edges from relations
  for (const rel of relations) {
    const sourceNode = `node-${rel.sourceId}`;
    const targetNode = `node-${rel.targetId}`;
    // Only add if both nodes exist
    if (memories.some((m) => m.id === rel.sourceId) && memories.some((m) => m.id === rel.targetId)) {
      const edgeColor = rel.relationType === 'causes'
        ? '#E63946'
        : rel.relationType === 'depends_on'
          ? '#FFB627'
          : rel.relationType === 'contradicts'
            ? '#D62828'
            : rel.relationType === 'supports'
              ? '#1B998B'
              : '#555555';
      edges.push({
        id: rel.id,
        source: sourceNode,
        target: targetNode,
        label: rel.relationType.replace(/_/g, ' '),
        weight: rel.strength,
        color: edgeColor,
      });
    }
  }

  // Create edges from memories to their agents
  for (const mem of memories) {
    edges.push({
      id: `edge-agent-${mem.id}`,
      source: `node-${mem.id}`,
      target: `node-agent-${mem.agentId}`,
      label: 'created by',
      weight: 0.5,
      color: '#888888',
    });
  }

  // Create edges from memories to concepts
  for (const mem of memories) {
    for (const concept of mem.concepts) {
      const conceptNodeId = `node-concept-${concept.replace(/\s+/g, '-')}`;
      if (sharedConcepts.includes(concept)) {
        edges.push({
          id: `edge-concept-${mem.id}-${concept.replace(/\s+/g, '-')}`,
          source: `node-${mem.id}`,
          target: conceptNodeId,
          label: 'about',
          weight: 0.3,
          color: '#AAAAAA',
        });
      }
    }
  }

  // Create edges from memories to projects
  for (const mem of memories) {
    if (mem.projectId) {
      edges.push({
        id: `edge-project-${mem.id}`,
        source: `node-${mem.id}`,
        target: `node-project-${mem.projectId}`,
        label: 'part of',
        weight: 0.6,
        color: '#7B2CBF',
      });
    }
  }

  return { nodes, edges };
}

// ─── Store ───

export const useMemoryStore = create<MemoryState>((set, get) => ({
  // ── State ──
  memories: SEED_MEMORIES,
  memoryRelations: SEED_RELATIONS,
  memoryExtractions: SEED_EXTRACTIONS,
  memoryTimeline: SEED_TIMELINE,
  graphNodes: [],
  graphEdges: [],
  memorySearchResults: [],
  memorySearchQuery: '',
  memorySearchType: 'all',
  selectedMemoryId: null,
  memoryGraphLayout: 'force',

  // ── Memory CRUD ──
  addMemory: (memory) =>
    set((state) => {
      const newMemories = [memory, ...state.memories];
      const { nodes, edges } = buildGraphFromMemories(newMemories, state.memoryRelations);
      return {
        memories: newMemories,
        graphNodes: nodes,
        graphEdges: edges,
      };
    }),

  updateMemory: (id, updates) =>
    set((state) => {
      const newMemories = state.memories.map((m) =>
        m.id === id ? { ...m, ...updates, updatedAt: Date.now() } : m,
      );
      const { nodes, edges } = buildGraphFromMemories(newMemories, state.memoryRelations);
      return {
        memories: newMemories,
        graphNodes: nodes,
        graphEdges: edges,
      };
    }),

  deleteMemory: (id) =>
    set((state) => {
      const newMemories = state.memories.filter((m) => m.id !== id);
      const newRelations = state.memoryRelations.filter(
        (r) => r.sourceId !== id && r.targetId !== id,
      );
      const newExtractions = state.memoryExtractions.filter((e) => e.sourceId !== id);
      const newTimeline = state.memoryTimeline.filter((t) => t.memoryId !== id);
      const { nodes, edges } = buildGraphFromMemories(newMemories, newRelations);
      return {
        memories: newMemories,
        memoryRelations: newRelations,
        memoryExtractions: newExtractions,
        memoryTimeline: newTimeline,
        graphNodes: nodes,
        graphEdges: edges,
        selectedMemoryId: state.selectedMemoryId === id ? null : state.selectedMemoryId,
      };
    }),

  referenceMemory: (id) =>
    set((state) => ({
      memories: state.memories.map((m) =>
        m.id === id
          ? { ...m, accessCount: m.accessCount + 1, lastReferencedAt: Date.now() }
          : m,
      ),
    })),

  // ── Relations ──
  addMemoryRelation: (relation) =>
    set((state) => {
      const newRelations = [...state.memoryRelations, relation];
      const { nodes, edges } = buildGraphFromMemories(state.memories, newRelations);
      return {
        memoryRelations: newRelations,
        graphNodes: nodes,
        graphEdges: edges,
      };
    }),

  removeMemoryRelation: (id) =>
    set((state) => {
      const newRelations = state.memoryRelations.filter((r) => r.id !== id);
      const { nodes, edges } = buildGraphFromMemories(state.memories, newRelations);
      return {
        memoryRelations: newRelations,
        graphNodes: nodes,
        graphEdges: edges,
      };
    }),

  // ── Extractions ──
  addExtraction: (extraction) =>
    set((state) => ({
      memoryExtractions: [extraction, ...state.memoryExtractions],
    })),

  confirmExtraction: (id) =>
    set((state) => ({
      memoryExtractions: state.memoryExtractions.map((e) =>
        e.id === id ? { ...e, status: 'confirmed' as ExtractionStatus } : e,
      ),
    })),

  rejectExtraction: (id) =>
    set((state) => ({
      memoryExtractions: state.memoryExtractions.map((e) =>
        e.id === id ? { ...e, status: 'rejected' as ExtractionStatus } : e,
      ),
    })),

  // ── Timeline ──
  addTimelineEvent: (event) =>
    set((state) => ({
      memoryTimeline: [event, ...state.memoryTimeline].slice(0, 200),
    })),

  // ── Search ──
  searchMemories: (query) => {
    const state = get();
    const results = performSearch(state.memories, query, state.memorySearchType);
    set({ memorySearchResults: results, memorySearchQuery: query });
  },

  // ── Getters ──
  getMemoryById: (id) => {
    return get().memories.find((m) => m.id === id);
  },

  getMemoriesByType: (type) => {
    return get().memories.filter((m) => m.type === type);
  },

  getMemoriesByAgent: (agentId) => {
    return get().memories.filter((m) => m.agentId === agentId);
  },

  getRelatedMemories: (memoryId) => {
    return get().memoryRelations.filter(
      (r) => r.sourceId === memoryId || r.targetId === memoryId,
    );
  },

  // ── Graph ──
  rebuildGraph: () =>
    set((state) => {
      const { nodes, edges } = buildGraphFromMemories(state.memories, state.memoryRelations);
      return { graphNodes: nodes, graphEdges: edges };
    }),

  // ── Setters ──
  setMemorySearchQuery: (query) => set({ memorySearchQuery: query }),

  setMemorySearchType: (type) => set({ memorySearchType: type }),

  setSelectedMemoryId: (id) => set({ selectedMemoryId: id }),

  setMemoryGraphLayout: (layout) => set({ memoryGraphLayout: layout }),
}));

// Initialize the graph on first import
const initialGraph = buildGraphFromMemories(SEED_MEMORIES, SEED_RELATIONS);
useMemoryStore.setState({
  graphNodes: initialGraph.nodes,
  graphEdges: initialGraph.edges,
});
